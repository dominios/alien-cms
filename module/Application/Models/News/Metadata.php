<?php

namespace Application\Models\News;

class Metadata extends \Application\Models\Utils\Metadata
{

    /**
     * Article URL.
     * @var string
     */
    protected $url;

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

}